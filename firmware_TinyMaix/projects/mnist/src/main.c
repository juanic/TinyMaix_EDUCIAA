int main(void)
{
	SystemClockInit();
	TM_DBGT_INIT();
	GPIOInit(GPIO_T_FIL3, GPIO_OUTPUT);
	GPIOOff(GPIO_T_FIL3);

	LedsInit();
	ILI9341Init(SPI_1, GPIO_1, GPIO_5, GPIO_3);
	ILI9341Rotate(ILI9341_Portrait_2);
	ILI9341DrawString(ILI9341_WIDTH/2-11*5, 20,"mnist demo", &font_11x18, ILI9341_BLACK, ILI9341_WHITE);
	ILI9341DrawPicture(ILI9341_WIDTH/2 - (28/2), 90, 28, 28, pic_3);
	/* Configuración de UART */
	UART_USB.baud_rate = 115200;
	UART_USB.port = SERIAL_PORT_PC;
	UART_USB.pSerial = NULL;
	UartInit(&UART_USB);

    TM_PRINTF("mnist demo\r\n");
    tm_mdl_t mdl;

    for(int i=0; i<28*28; i++){
        TM_PRINTF("%3d,", mnist_pic[i]);
        if(i%28==27)TM_PRINTF("\r\n");
    }

    tm_mat_t in_uint8 = {3,28,28,1, (mtype_t*)mnist_pic};
    tm_mat_t in = {3,28,28,1, NULL};
    tm_mat_t outs[1];
    tm_err_t res;
    tm_stat((tm_mdlbin_t*)mdl_data);

    res = tm_load(&mdl, mdl_data, NULL, layer_cb, &in);
    if(res != TM_OK) {
        TM_PRINTF("tm model load err %d\r\n", res);
        return -1;
    }

#if TM_MDL_TYPE != TM_MDL_FP32
    res = tm_preprocess(&mdl, TMPP_UINT2INT, &in_uint8, &in);
#else
    res = tm_preprocess(&mdl, TMPP_UINT2FP01, &in_uint8, &in);
#endif
    TM_DBGT_START();
    GPIOOn(GPIO_T_FIL3);
    res = tm_run(&mdl, &in, outs);
    GPIOOff(GPIO_T_FIL3);
    TM_DBGT("tm_run");
    if(res==TM_OK) parse_output(outs);
    else TM_PRINTF("tm run error: %d\r\n", res);
    tm_unload(&mdl);
    return 0;
}

