static tm_err_t layer_cb(tm_mdl_t* mdl, tml_head_t* lh)
{   //dump middle result
    int h = lh->out_dims[1];
    int w = lh->out_dims[2];
    int ch= lh->out_dims[3];
    mtype_t* output = TML_GET_OUTPUT(mdl, lh);
    return TM_OK;
    TM_PRINTF("Layer %d callback ========\r\n", mdl->layer_i);
    #if 1
    for(int y=0; y<h; y++){
        TM_PRINTF("[");
        for(int x=0; x<w; x++){
            TM_PRINTF("[");
            for(int c=0; c<ch; c++){
            #if TM_MDL_TYPE == TM_MDL_FP32
                TM_PRINTF("%.3f,", output[(y*w+x)*ch+c]);
            #else
                TM_PRINTF("%.3f,", TML_DEQUANT(lh,output[(y*w+x)*ch+c]));
            #endif
            }
            TM_PRINTF("],");
        }
        TM_PRINTF("],\n");
    }
    TM_PRINTF("\n");
    #endif
    return TM_OK;
}

static void parse_output(tm_mat_t* outs)
{
    tm_mat_t out = outs[0];
    float* data  = out.dataf;
    float maxp = 0;
    int maxi = -1;
    for(int i=0; i<10; i++){
        printf("%d: %.3f\r\n", i, data[i]);
        if(data[i] > maxp) {
            maxi = i;
            maxp = data[i];
        }
    }
    TM_PRINTF("### Predict output is: Number %d\r\n", maxi);
    ILI9341DrawString(MARGIN, 150,"Predict output is:", &font_11x18, ILI9341_BLACK, ILI9341_WHITE);
    ILI9341DrawInt(ILI9341_WIDTH/2, 190, maxi , 1, &font_11x18, ILI9341_BLACK, ILI9341_WHITE);
    ILI9341DrawString(MARGIN, 230,"Prob:", &font_11x18, ILI9341_BLACK, ILI9341_WHITE);
    ILI9341DrawInt(MARGIN + 11*6, 230, maxp*100 , 2, &font_11x18, ILI9341_BLACK, ILI9341_WHITE);
    ILI9341DrawString(MARGIN + 11*8, 230,"%", &font_11x18, ILI9341_BLACK, ILI9341_WHITE);
    return;
}
